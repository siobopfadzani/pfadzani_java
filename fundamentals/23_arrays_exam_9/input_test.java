import java.util.Scanner;
public class loop_test
{
	public static void main(String[]args)
	{
		Scanner scan = new Scanner(System.in);
		int n;
		for (int i = 0;i <= 5;i++)
		{
			for (int k = 0; k <= i;k++)
			{
				n = scan.nextInt;
			}
				if (n < 1||n>5)
				{
					System.out.println();
				}
				System.out.println(n+ " ");
		}

	}
}