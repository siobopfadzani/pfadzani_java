public class MultiArrayExample {
    public static void main(String[] args) {
        // Creating a 2D array
        int[][] matrix = {
            {1, 2, 3},
            {4, 5, 6},
            {7, 8, 9}
        };

        // Accessing elements using enhanced for loops
        System.out.println("Using Enhanced For Loops:");
        for (int[] row : matrix) {
            for (int element : row) {
                System.out.print(element + " ");
            }
            System.out.println();
        }
    }
}
